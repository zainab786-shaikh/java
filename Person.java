public class Person{

    private int age;
    private boolean isMarried;
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public boolean isMarried() {
        return isMarried;
    }
    public void setMarried(boolean isMarried) {
        this.isMarried = isMarried;
    }
    
}